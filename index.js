const express = require('express');
const puppeteer = require('puppeteer');

const app = express();
const PORT = process.env.PORT || 3000;

app.get('/', async (req, res) => {
  try {
    const browser = await puppeteer.launch({
      headless: true,
      args: ['--no-sandbox', '--disable-setuid-sandbox']
    });
    const page = await browser.newPage();

    await page.goto('https://ai.invideo.io', { waitUntil: 'networkidle2' });
    console.log('âœ… InVideo AI opened');
    await page.waitForTimeout(30000);

    await page.goto('https://ai.invideo.io/create', { waitUntil: 'networkidle2' });
    await page.keyboard.type('This is a sample TikTok script generated by Aeulin.');
    await page.waitForTimeout(10000);

    await browser.close();
    console.log('âœ… Puppeteer script complete');
    res.send('âœ… Puppeteer script executed and browser closed.');
  } catch (error) {
    console.error('âŒ Puppeteer failed:', error);
    res.status(500).send('âŒ Something went wrong.');
  }
});

app.listen(PORT, () => {
  console.log(`ğŸš€ Server listening on port ${PORT}`);
});
